// Program to find the sum of two integers.

#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter the first integer");
    scanf("%d",&a);
    printf("Enter the second integer");
    scanf("%d",&b);
    printf("The sum of %d and %d is %d",a,b,a+b);
    return 0;
}



