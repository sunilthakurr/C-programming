//program to find area of the rectangle
#include<stdio.h>
int
main ()
{
  float l, b, A;
  //where is l is length ,b is breadth and A is area of rectangle
  printf ("Enter the length of rectangle");
  scanf ("%f", &l);
  printf ("Enter the breadth of rectangle");
  scanf ("%f", &b);
  A = l * b;
  printf("Area of rectangle is %f",A);
  return 0;

}
